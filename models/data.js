let mongoose = require("mongoose");
let Schema = mongoose.Schema;

let dataSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
});

let data = mongoose.model("data", dataSchema);
module.exports = data;
